#include "libft.h"

/*
** '\t' = 9
** '\n' = 10
** '\v' = 11
** '\f' = 12
** '\r' = 13
** ' '  = 32
*/

int		ft_isspace(int c)
{
	return (c == 32 || (c >= 9 && c <= 13));
}
