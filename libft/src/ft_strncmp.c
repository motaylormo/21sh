#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	if (!n)
		return (0);
	while (*s1 == *s2 && n--)
	{
		if (!n)
			return (0);
		if (*s1 != *s2 || (!*s1 || !*s2))
			break ;
		s1++;
		s2++;
	}
	return ((uint8_t)(*s1) - (uint8_t)(*s2));
}
