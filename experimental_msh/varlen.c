int			msh_varlen(const char *s)
{
	register int	i;
	char			*p;

	p = (char*)s;
	if (!s || !p)
		return (0);
	if (g_varctbl[(int)p[0]] & 4)
		p++;
	i = 0;
	while (g_varctbl[(int)p[i]] & 3)
		i++;
	return (i);
}
